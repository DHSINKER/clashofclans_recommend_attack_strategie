DROP DATABASE IF EXISTS `clashofclansdb`;

CREATE DATABASE IF NOT EXISTS `clashofclansdb` 
  DEFAULT CHARACTER SET utf8 
  DEFAULT COLLATE utf8_general_ci;

USE `clashofclansdb` ;

CREATE TABLE `troop_info` (
  `id` INT NOT NULL,
  `name` VARCHAR(255) NOT NULL,
  `kor_name` VARCHAR(255) NOT NULL,
  `image` VARCHAR(2083) NULL,
  PRIMARY KEY (`id`)
) ENGINE = InnoDB
  DEFAULT CHARACTER SET utf8 
  DEFAULT COLLATE utf8_general_ci;
  
  CREATE TABLE `spell_info` (
  `id` INT NOT NULL,
  `name` VARCHAR(255) NOT NULL,
  `kor_name` VARCHAR(255) NOT NULL,
  `image` VARCHAR(2083) NULL,
  PRIMARY KEY (`id`)
) ENGINE = InnoDB
  DEFAULT CHARACTER SET utf8 
  DEFAULT COLLATE utf8_general_ci;
  
  CREATE TABLE `hero_info` (
  `id` INT NOT NULL,
  `name` VARCHAR(255) NOT NULL,
  `kor_name` VARCHAR(255) NOT NULL,
  `image` VARCHAR(2083) NULL,
  PRIMARY KEY (`id`)
) ENGINE = InnoDB
  DEFAULT CHARACTER SET utf8 
  DEFAULT COLLATE utf8_general_ci;
  
  
  
CREATE TABLE `troop_upgrade` (
  `id` INT NOT NULL,
  `troop_id` INT NOT NULL,
  `troop_level` INT NOT NULL,
  `min_TH` INT NOT NULL,
  `upgrade_time` INT NOT NULL,
  PRIMARY KEY (`id`),
  FOREIGN KEY (`troop_id`) REFERENCES `troop_info` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE = InnoDB
  DEFAULT CHARACTER SET utf8 
  DEFAULT COLLATE utf8_general_ci;  
  
CREATE TABLE `spell_upgrade` (
  `id` INT NOT NULL,
  `spell_id` INT NOT NULL,
  `spell_level` INT NOT NULL,
  `min_TH` INT NOT NULL,
  `upgrade_time` INT NOT NULL,
  PRIMARY KEY (`id`),
  FOREIGN KEY (`spell_id`) REFERENCES `spell_info` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE = InnoDB
  DEFAULT CHARACTER SET utf8 
  DEFAULT COLLATE utf8_general_ci;  
  
CREATE TABLE `hero_upgrade` (
  `id` INT NOT NULL,
  `hero_id` INT NOT NULL,
  `hero_level` INT NOT NULL,
  `min_TH` INT NOT NULL,
  `upgrade_time` INT NOT NULL,
  PRIMARY KEY (`id`),
  FOREIGN KEY (`hero_id`) REFERENCES `hero_info` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE = InnoDB
  DEFAULT CHARACTER SET utf8 
  DEFAULT COLLATE utf8_general_ci;  
  
  
  
CREATE TABLE `super_troop` (
  `id` INT NOT NULL,
  `base_troop` INT NOT NULL,
  `name` VARCHAR(255) NOT NULL,
  `kor_name` VARCHAR(255) NOT NULL,
  `min_level` INT NOT NULL,
  `image` VARCHAR(2083) NULL,
  PRIMARY KEY (`id`),
  FOREIGN KEY (`base_troop`) REFERENCES `troop_info` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE = InnoDB
  DEFAULT CHARACTER SET utf8 
  DEFAULT COLLATE utf8_general_ci;  
  
  
  
CREATE TABLE `base_set` (
  `id` INT NOT NULL,
  `base` VARCHAR(255) NOT NULL,
  `kor_name` VARCHAR(255) NOT NULL,
  PRIMARY KEY (`id`)
) ENGINE = InnoDB
  DEFAULT CHARACTER SET utf8 
  DEFAULT COLLATE utf8_general_ci;  
  
  
  
CREATE TABLE `war_set` (
  `id` INT NOT NULL,
  `base_unit` INT NOT NULL,
  `kor_name` VARCHAR(255) NOT NULL,
  `townhall` INT NOT NULL,
  `super` INT NULL,
  `main` INT NULL,
  `troop1` INT NULL,
  `troop2` INT NULL,
  `troop3` INT NULL,
  `spell1` INT NULL,
  `spell2` INT NULL,
  `spell3` INT NULL,
  `spell4` INT NULL,
  `hero` INT NULL,
  PRIMARY KEY (`id`),
  FOREIGN KEY (`base_unit`) REFERENCES `base_set` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (`super`) REFERENCES `super_troop` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (`main`) REFERENCES `troop_info` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (`troop1`) REFERENCES `troop_info` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (`troop2`) REFERENCES `troop_info` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (`troop3`) REFERENCES `troop_info` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (`spell1`) REFERENCES `spell_info` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (`spell2`) REFERENCES `spell_info` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (`spell3`) REFERENCES `spell_info` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (`spell4`) REFERENCES `spell_info` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (`hero`) REFERENCES `hero_info` (`id`)
  ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE = InnoDB
  DEFAULT CHARACTER SET utf8 
  DEFAULT COLLATE utf8_general_ci;  
  
  

